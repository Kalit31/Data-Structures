void solve()
{
    string n, m;
    cin >> n >> m;
    int nl = n.size();
    int ml = m.size();

    vector<vector<int>> dp(nl + 1, vector<int>(ml + 1, 0));
    for (int i = 0; i <= nl; i++)
    {
        for (int j = 0; j <= ml; j++)
        {
            if (i == 0)
            {
                dp[i][j] = j;
            }
            else if (j == 0)
            {
                dp[i][j] = i;
            }
            else if (n[i - 1] == m[j - 1])
            {
                dp[i][j] = dp[i - 1][j - 1];
            }
            else
            {
                dp[i][j] = 1 + min(dp[i - 1][j], min(dp[i - 1][j - 1], dp[i][j - 1]));
            }
        }
    }
    cout << dp[nl][ml] << endl;
}

