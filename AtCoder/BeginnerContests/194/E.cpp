ll gcd(ll a, ll b)
{
    if (b == 0)
    {
        return a;
    }
    return gcd(b, a % b);
}

ll my_pow(ll a, ll n, ll m = INF)
{
    ll res = 1;
    while (n)
    {
        if (n % 2)
        {
            res = (res * a) % m;
            n--;
        }
        a = (a * a) % m;
        n /= 2;
    }
    return res;
}

void solve()
{
    int n, m;
    cin >> n >> m;
    set<int> ele;
    for (int i = 0; i <= n; i++)
    {
        ele.insert(i);
    }
    vector<int> cnt(n, 0);
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < m; i++)
    {
        cnt[a[i]]++;
        ele.erase(a[i]);
    }
    int ans = *(ele.begin());
    for (int i = m; i < n; i++)
    {
        cnt[a[i - m]]--;
        if (cnt[a[i - m]] == 0)
        {
            ele.insert(a[i - m]);
        }
        cnt[a[i]]++;
        ele.erase(a[i]);
        ans = min(ans, *(ele.begin()));
    }
    cout << ans << endl;
}

