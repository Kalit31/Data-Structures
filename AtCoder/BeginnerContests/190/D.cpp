ll gcd(ll a, ll b)
{
    if (b == 0)
    {
        return a;
    }
    return gcd(b, a % b);
}

ll my_pow(ll a, ll n, ll m = INF)
{
    ll res = 1;
    while (n)
    {
        if (n % 2)
        {
            res = (res * a) % m;
            n--;
        }
        a = (a * a) % m;
        n /= 2;
    }
    return res;
}

void solve()
{
    ll n;
    cin >> n;
    n = n * 2;
    ll cnt = 0;
    for (ll i = 1; i <= sqrt(n); i++)
    {
        if (n % i == 0)
        {
            ll opt1 = i;
            ll opt2 = n / i;
            ll k = opt1 - 1;
            ll b = opt2;
            if (abs(k - b) % 2 == 0)
            {
                cnt++;
            }
            if (opt1 == opt2)
            {
                continue;
            }
            k = opt2 - 1;
            b = opt1;
            if (abs(k - b) % 2 == 0)
            {
                cnt++;
            }
        }
    }
    cout << cnt << endl;
}

