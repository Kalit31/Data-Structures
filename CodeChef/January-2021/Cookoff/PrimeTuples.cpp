void SieveOfEratosthenes(int n)
{
    bool prime[n + 1];
    memset(prime, true, sizeof(prime));

    for (int p = 2; p * p <= n; p++)
    {

        if (prime[p] == true)
        {

            for (int i = p * p; i <= n; i += p)
                prime[i] = false;
        }
    }

    primes.push_back(2);

    for (int p = 3; p <= n; p++)
        if (prime[p])
        {
            if (p - primes.back() == 2)
            {
                pref[p] = pref[p - 1] + 1;
            }
            else
            {
                pref[p] = pref[p - 1];
            }
            primes.push_back(p);
        }
        else
        {
            pref[p] = pref[p - 1];
        }
}

