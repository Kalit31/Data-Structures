ll gcd(ll a, ll b)
{
    if (b == 0)
    {
        return a;
    }
    return gcd(b, a % b);
}

ll my_pow(ll a, ll n, ll m = INF)
{
    ll res = 1;
    while (n)
    {
        if (n % 2)
        {
            res = (res * a) % m;
            n--;
        }
        a = (a * a) % m;
        n /= 2;
    }
    return res;
}

void solve()
{
    int n;
    cin >> n;
    vector<int> h(n);
    vector<int> iq(n);
    for (int i = 0; i < n; i++)
    {
        cin >> h[i];
    }
    for (int i = 0; i < n; i++)
    {
        cin >> iq[i];
    }
    vector<array<int, 2>> a(n);
    for (int i = 0; i < n; i++)
    {
        a[i] = {h[i], iq[i]};
    }
    vector<int> dp(n, 1);
    for (int i = 0; i < n; i++)
    {
        for (int j = i + 1; j < n; j++)
        {
            if (a[i][0] < a[j][0] && a[i][1] > a[j][1] && dp[i] + 1 > dp[j])
            {
                dp[j] = dp[i] + 1;
            }
        }
    }
    cout << *max_element(dp.begin(), dp.end()) << endl;
}

