void solve()
{
    int n, k;
    cin >> n >> k;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int ans = 0;

    for (int i = 0; i < k; i++)
    {
        map<int, int> freq;
        int tot = 0;
        for (int j = i; j < n; j += k)
        {
            freq[a[j]]++;
            tot++;
        }
        int maxOccurence = 0;
        for (auto f : freq)
        {
            maxOccurence = max(maxOccurence, f.second);
        }
        ans += (tot - maxOccurence);
    }
    cout << ans << endl;
}

